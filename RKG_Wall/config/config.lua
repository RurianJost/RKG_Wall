function CONFIG.NOTIFY(...)
    local args = { ... }

    local notifyTypes = {
        [1] = 'denied',
        [2] = 'sucess',
        [3] = 'warn'
    }

    if IsDuplicityVersion() then 
        local source = args[1]

        TriggerClientEvent('Notify', source, notifyTypes[args[2]], args[3], args[4])
    else 
        TriggerEvent('Notify', notifyTypes[args[1]], args[2], args[3])
    end 
end

